using System;

class DeclareFiveVariables
{
    static void Main()
    {
        ushort firstVariable = 52130;
        sbyte secondVariable = -115;
        uint thirdVariable = 4825932;
        byte fourthVariable = 97;
        short fifthVariable = -10000;

        Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}", firstVariable, secondVariable, thirdVariable, fourthVariable, fifthVariable);
    }
}